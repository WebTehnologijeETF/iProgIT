<h1> Koga tražimo?</h1>
			 <p class = "desc">

Ohrabrujemo kandidate koji mogu imati značajne koristi od školovanja koje je, na prvom mestu, okrenuto praksi i osposobljavanju za kvalitetno plaćene poslove sa računarima. Da bismo stvorili dinamičnu atmosferu prenosa novih veština između profesora i polaznika, tražimo raznolikost među polaznicima.
     	   </p>

     	  <h1> Šta dobijate? </h1>
                     <p   class="desc">
Dobićete veštine i znanja da postanete stručnjak za napredan rad sa računarima. Dobićete resurse i alate da svoje znanje odlično naplatite i uživate u svojim veštinama. Steći ćete zvanične sertifikate, izdate od strane Cambridge odeljenja za međunarodne ispite, vodećih softverskih korporacija Microsoft i Adobe, kao i od strane tri svetske IT organizacije. 

Školovanje je jednogodišnje i počinje jesenjim semestrom 2015. godine. Kao polaznik, birate način nastave i obuke: tradicionalno – u učionici ili online – na daljinu.


                 </p>
 <h1>Apsolutna garancija veština i znanja </h1>
                     <p   class="desc">


Ovo je naša garancija Vama: obučićemo Vas za visokoplaćenu profesionalnu karijeru. Ako na kraju školovanja iz bilo kog razloga mislite da niste savladali sva znanja i veštine koje smo obrađivali na ITAcademy, možete da se školujete sledeće godine, besplatno. 

Znamo koliko je visok kvalitet školovanja na ITAcademy i šta sve možete postići posle školovanja, tako da smo slobodni da Vam damo ovakvu garanciju.


                 </p>

 <h1>Ko može da se prijavi? </h1>
                     <p   class="desc">

Ako ste već počeli sa jednogodišnjim školovanjem na nekoj drugoj školi, niste kvalifikovani da se prijavite. Od kandidata prihvatamo prijave za upis školske godine koja počinje jesenjim semestrom.  

Engleski jezik ne morate da znate. Sva predavanja na ITAcademy su na srpskom jeziku. Pri tom, svaki polaznik ima obezbeđenu jednogodišnju nastavu engleskog jezika.


                 </p>

                  <h1>Za pravu osobu, vreme za prijavu je upravo sada </h1>
                     <p   class="desc">

Izbor pouzdanog životnog puta nije vezan za prošlost. Naši polaznici se nalaze u raznim životnim pozicijama - od mladih i ambicioznih do zrelih, iskusnih i dokazanih. Mladi su odlučili da što pre preuzmu upotrebljive veštine koje će lako moći da naplate, dok zaposleni profesionalci žele sigurniju karijeru, još bolju poziciju, veću platu. Svi oni žele posao koji će ih ispuniti, duhovno i finansijski. 

U vezi sa tim, osoba koja najbolje može da odredi da li ste Vi trenutno spremni za prijavu na listu polaznika ste upravo Vi sami.

                 </p>